package main
import "fmt"
type Books struct{
    title string
    id int
}
    
func main(){
    var book1 Books
    var book2 Books
    
    book1.title = "Golang"
    book1.id = 1
    
    book2.title= "SHASHI is learnin"
    book2.id = 2
    
    fmt.Printf("book title %s\n", book1.title)
    fmt.Printf("book id %d\n", book1.id)
    fmt.Printf("book title %s\n", book2.title)
    fmt.Printf("book title %d\n", book2.id)
}


